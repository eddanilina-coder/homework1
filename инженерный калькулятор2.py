import math

class Calculator:
    def __init__(self):
        pass

    def add(self, a, b):
        return a + b

    def subtract(self, a, b):
        return a - b

    def multiply(self, a, b):
        return a * b

    def divide(self, a, b):
        if b == 0:
            return "Ошибка: Деление на ноль"
        return a / b

    def power(self, a, b):
        return a ** b

    def sqrt(self, a):
        if a < 0:
            return "Ошибка: Корень из отрицательного числа"
        return math.sqrt(a)

    def sin(self, angle_degrees):
        return math.sin(math.radians(angle_degrees))

    def cos(self, angle_degrees):
        return math.cos(math.radians(angle_degrees))

    def tan(self, angle_degrees):
        return math.tan(math.radians(angle_degrees))

    def log(self, a): # Натуральный логарифм
        if a <= 0:
            return "Ошибка: Логарифм не положительного числа"
        return math.log(a)

    def log10(self, a): # Десятичный логарифм
        if a <= 0:
            return "Ошибка: Логарифм не положительного числа"
        return math.log10(a)
    
